use chrono::Local;
use env_logger::Builder;
use log;
use log::LevelFilter;
use std::io::Write;

pub fn init_logging() {
    Builder::new()
        .format(|buf, record| {
            writeln!(buf,
                     "{} [{}] - {}",
                     Local::now().format("%Y-%m-%dT%H:%M:%S"),
                     record.level(),
                     record.args()
            )
        })
        .filter(None, LevelFilter::Info)
        .init();
}
